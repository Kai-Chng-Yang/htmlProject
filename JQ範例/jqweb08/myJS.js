$(function () {

    $("#BBB").click(function () {
        $("#CON_BOX").css("color", "#00F").css("font-size", "30px")
    });

    $("#MMM").click(function () {
        $("#CON_BOX").css({ "color": "#099", "font-size": "14px" })
    });

    $("#SSS").click(function () {
        $("#CON_BOX").css({ fontSize: 11 })
    })










})